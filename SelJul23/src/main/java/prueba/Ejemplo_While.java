package prueba;

public class Ejemplo_While {

	public static void main(String[] args) {
		int a = 1;
		
		//Salir cuando del bucle cuando "a" es mayor que 4
		while (a <= 4) {
			System.out.println("El valor de A: " + a);
			//incremante el calor de a para la siguiente iteracion
			a++;
		}

	}

}
